from turtle import *
penup()
goto(50,50)
pendown()
 
pencolor('gold')
fillcolor('gold')
begin_fill()
fd(100)
setheading(270)
fd(100)
setheading(180)
fd(100)
setheading(270)
fd(100)
setheading(180)
fd(100)
setheading(90)
fd(150)
setheading(0)
fd(100)
setheading(90)
fd(50)
end_fill()
 
begin_fill()
pencolor('dodgerblue')
fillcolor('dodgerblue')
fd(100)
setheading(180)
fd(100)
setheading(270)
fd(100)
setheading(180)
fd(100)
setheading(270)
fd(100)
setheading(0)
fd(100)
setheading(90)
fd(50)
setheading(0)
fd(100)
setheading(90)
fd(50)
end_fill()
 
penup()
pencolor('black')
fillcolor('black')
goto(-50,100)
setheading(0)
pendown()
fd(50)
 
penup()
goto(50,-100)
setheading(180)
pendown()
fd(50)
 
penup()
pencolor('white')
fillcolor('white')
goto(-25,125)
pendown()
pensize(10)
circle(5)
 
penup()
goto(25,-125)
pendown()
pensize(10)
circle(5)
 
penup()
goto(0,-200)
pencolor('black')
fillcolor('black')
write('Python',align='center',font=('宋体',20,'normal'))
 
ht()
 
done()
 
