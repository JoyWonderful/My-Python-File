from turtle import *
bgcolor('black')
speed(5)

#红方块
pendown()
pencolor('red')
fillcolor('red')
begin_fill()
rt(180)
fd(200)
rt(90)
fd(200)
rt(90)
fd(200)
rt(90)
fd(200)
end_fill()
penup()

#绿方块
goto(10,0)
pendown()
setheading(90)
pencolor('green')
fillcolor('green')
begin_fill()
fd(200)
rt(90)
fd(200)
rt(90)
fd(200)
rt(90)
fd(200)
end_fill()
penup()

#黄方块
goto(10,-10)
pendown()
setheading(-90)
pencolor('yellow')
fillcolor('yellow')
begin_fill()
fd(200)
rt(-90)
fd(200)
rt(-90)
fd(200)
rt(-90)
fd(200)
end_fill()
penup()

#蓝方块
goto(0,-10)
pendown()
setheading(-90)
pencolor('blue')
fillcolor('blue')
begin_fill()
fd(200)
rt(90)
fd(200)
rt(90)
fd(200)
rt(90)
fd(200)
end_fill()
penup()

#写Microsoft
goto(0,-260)
pencolor('white')
fillcolor('white')
write('Microsoft',align='center',font=('微软雅黑',30,'normal'))

done()

