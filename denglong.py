from turtle import *
title("灯笼")
setup(350,350,startx=None,starty=None)
pensize(3)
pencolor("black")
fillcolor("black")

penup()
goto(0,100)
setheading(270)
pendown()
fd(30)

pencolor("gold")
fillcolor("yellow")
penup()
goto(-20,70)
setheading(0)
pendown()
begin_fill()
for x in range (2):
    fd(40)
    rt(90)
    fd(15)
    rt(90)
end_fill()

pencolor("red")
fillcolor("tomato")
penup()
goto(-45,-45)
pendown()
begin_fill()
fd(90)
circle(50,180)
fd(90)
circle(50,180)
end_fill()

color("gold")
penup()
goto(-5,55)
pendown()
setheading(180)
circle(60,135)

penup()
goto(0,55)
setheading(270)
pendown()
fd(100)

penup()
goto(5,55)
setheading(180)
pendown()
circle(60,-135)

pencolor("gold")
fillcolor("yellow")
penup()
goto(-20,-45)
setheading(0)
pendown()
begin_fill()
for x in range (2):
    fd(40)
    rt(90)
    fd(15)
    rt(90)
end_fill()

penup()
pensize(1)
goto(-15,-60)
color("gold")
setheading(270)
pendown()
fd(30)

penup()
pensize(1)
goto(-10,-60)
color("gold")
setheading(270)
pendown()
fd(30)

penup()
pensize(1)
goto(-5,-60)
color("gold")
setheading(270)
pendown()
fd(30)

penup()
pensize(1)
goto(0,-60)
color("gold")
setheading(270)
pendown()
fd(30)

penup()
pensize(1)
goto(5,-60)
color("gold")
setheading(270)
pendown()
fd(30)

penup()
pensize(1)
goto(10,-60)
color("gold")
setheading(270)
pendown()
fd(30)

penup()
pensize(1)
goto(15,-60)
color("gold")
setheading(270)
pendown()
fd(30)

hideturtle()
done()

